def ensure_master_header():
    if MASTER_PATH.exists():
        with MASTER_PATH.open("r", encoding="utf-8-sig", newline="") as f:
            first = f.readline().strip("\n\r")
        if first.split(",") == MASTER_COLUMNS:
            return
    with MASTER_PATH.open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(MASTER_COLUMNS)


def load_existing_sets():
    existing_timestamps: set[str] = set()
    existing_sources: set[str] = set()
    if not MASTER_PATH.exists():
        return existing_timestamps, existing_sources
    with MASTER_PATH.open("r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for r in reader:
            ts = (r.get("Timestamp") or "").strip()
            sf = (r.get("Source File") or "").strip()
            if ts:
                existing_timestamps.add(ts)
            if sf:
                existing_sources.add(sf)
    return existing_timestamps, existing_sources


def parse_export_csv(text: str):
    f = io.StringIO(text)
    reader = csv.DictReader(f)
    fields = [c.strip().lower() for c in (reader.fieldnames or [])]
    if len(fields) < 4 or fields[0] != "index":
        raise ValueError(f"Unexpected export columns: {reader.fieldnames}")
    idx_key = reader.fieldnames[0]
    ts_key = reader.fieldnames[1]
    temp_key = reader.fieldnames[2]
    rh_key = reader.fieldnames[3]

    out = []
    for r in reader:
        idx = (r.get(idx_key) or "").strip()
        ts_raw = (r.get(ts_key) or "").strip()
        temp = (r.get(temp_key) or "").strip()
        rh = (r.get(rh_key) or "").strip()
        if not ts_raw:
            continue
        dt = datetime.strptime(ts_raw, "%Y-%m-%d %H:%M:%S")
        ts = dt.strftime("%Y-%m-%d %H:%M:%S")
        date = dt.strftime("%Y-%m-%d 00:00:00")
        time = dt.strftime("%H:%M:%S")
        out.append((idx, ts, date, time, temp, rh))
    return out


def main():
    ensure_master_header()
    existing_timestamps, existing_sources = load_existing_sets()

    emails = 3
    attachments = 0
    added_rows = 0
    skipped_whole_files = 0
    skipped_dupe_timestamps = 0

    with MASTER_PATH.open("a", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=MASTER_COLUMNS)
        for (filename, export_csv) in ATTACHMENTS:
            attachments += 1
            if filename in existing_sources:
                skipped_whole_files += 1
                continue
            rows = parse_export_csv(export_csv)
            for (src_row, ts, date, time, temp, rh) in rows:
                if ts in existing_timestamps:
                    skipped_dupe_timestamps += 1
                    continue
                writer.writerow(
                    {
                        "Source File": filename,
                        "Source Row": src_row,
                        "Timestamp": ts,
                        "Date": date,
                        "Time": time,
                        "Temperature (C)": temp,
                        "Relative Humidity (%)": rh,
                    }
                )
                existing_timestamps.add(ts)
                existing_sources.add(filename)
                added_rows += 1

    print(
        f"emails={emails} attachments={attachments} added_rows={added_rows} "
        f"skipped_whole_files={skipped_whole_files} skipped_dupe_timestamps={skipped_dupe_timestamps}"
    )


if __name__ == "__main__":
    main()

